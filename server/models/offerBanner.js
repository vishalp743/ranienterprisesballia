const mongoose = require('mongoose');

const offerBannerSchema = new mongoose.Schema({
  image1: {
    type: String,
    required: true
  },
  image2: {
    type: String,
    required: true
  },
  image3: {
    type: String,
    required: true
  },
  createdAt: {
    type: Date,
    default: Date.now
  }
});

module.exports = mongoose.model('OfferBanner', offerBannerSchema);